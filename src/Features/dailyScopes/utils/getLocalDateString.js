// Local calendar day as "YYYY-MM-DD" (the client resolves the timezone,
// the backend filters on this civil day).

export default function getLocalDateString(date = new Date()) {
  const y = date.getFullYear();
  const m = String(date.getMonth() + 1).padStart(2, "0");
  const d = String(date.getDate()).padStart(2, "0");
  return `${y}-${m}-${d}`;
}
